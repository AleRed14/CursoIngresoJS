/*
al presionar el botón mostrar 10 repeticiones 
con números ASCENDENTE, desde el 1 al 10.*/
function mostrar()
{
	var numeroAscendente;
	numeroAscendente = 1

	while (numeroAscendente < 11) {
		alert(numeroAscendente);
		numeroAscendente = numeroAscendente + 1;
	}
	
}//FIN DE LA FUNCIÓN